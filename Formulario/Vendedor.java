    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:
         if(txtnombrecompleto.getText().length()== 0 ){
        JOptionPane.showMessageDialog(rootPane, "Ingrese El Nombre");
    }
    if(txtbasededatos.getText().length() ==0 ){
       JOptionPane.showMessageDialog(rootPane, "Ingrese La Base De Datos"); 
    } 
    if(txtcedula.getText().length() == 0 ){
       JOptionPane.showMessageDialog(rootPane, "Ingrese La Cedula"); 
    }
    else {
    String non,base,cedula,stock;
            String sql="";
            non=txtnombrecompleto.getText();
            base=txtbasededatos.getText();
            cedula=txtcedula.getText(); 
            sql="INSERT INTO vendedor ( idvendedor,nombrecompleto,basedatos) VALUES (?,?,? )";
        try {
            PreparedStatement pst  = cn.prepareStatement(sql);
            pst.setString(1, cedula);
            pst.setString(2, non);
            pst.setString(3, base); 
            
            int n=pst.executeUpdate();
            if(n>0){
            JOptionPane.showMessageDialog(null, "Registro Guardado con Exito");
            bloquear();
            }
            limpiar();
            cargar("");
        } catch (SQLException ex) {
            Logger.getLogger(IngresoProductos.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    }//GEN-LAST:event_btnguardarActionPerformed

