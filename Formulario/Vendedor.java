void cargar(String valor) {
        try{
            String [] titulos={"codigo","nombre completo","Base Datos"};
            String [] registros= new String[3];
            model=new DefaultTableModel(null,titulos);
            
            String cons="select * from vendedor where nombrecompleto LIKE '%"+valor+"%'";
             
            Statement st= cn.createStatement();
            ResultSet rs = st.executeQuery(cons);
            while(rs.next()){
                
                registros[0]=rs.getString("idvendedor");
                registros[1]=rs.getString("nombrecompleto");
                registros[2]=rs.getString("basedatos"); 
             
                
                model.addRow(registros);      
                }
            jtvendednro.setModel(model); 
            }catch(Exception e){
                System.out.println(e.getMessage());
                 }
     
    }
