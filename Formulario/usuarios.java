private void btnregistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistrarActionPerformed

    if(txtid.getText().length()== 0){
        JOptionPane.showMessageDialog(rootPane, "Ingrese La Identidicacion / Codigo");
    }
    if(txtnick.getText().length()== 0){
        JOptionPane.showMessageDialog(rootPane, "Ingrese La Nombre De Usuario / Nick");
    }
    if(txtpass.getText().length()== 0){
        JOptionPane.showMessageDialog(rootPane, "Ingrese Una Contraseña");
    }
    if(cbotipo.getSelectedIndex()==0){
        JOptionPane.showMessageDialog(rootPane, "Seleccione Tipo Usuario");
    }
    
    String ins="INSERT INTO usuario (idusuario,nick, password,tipousuario) VALUES(?,?,?,?)";
        try {
            PreparedStatement pst = cn.prepareStatement(ins);
            pst.setString(1, txtid.getText());
            pst.setString(2, txtnick.getText());
            pst.setString(3,new String(txtpass.getPassword()));
            pst.setString(4, cbotipo.getSelectedItem().toString());
            int n= pst.executeUpdate();
            if(n>0)
            {
                JOptionPane.showMessageDialog(this, "Se guardaron los datos");
                mostrarusuarios();
                txtid.setText(null);
                txtnick.setText(null);
                txtpass.setText(null);
                cbotipo.setSelectedIndex(0);
                Generarnumeracion();
            }
            else
            {
                 JOptionPane.showMessageDialog(this, "Error");
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(usuarios.class.getName()).log(Level.SEVERE, null, ex);
        }

}//GEN-LAST:event_btnregistrarActionPerformed

